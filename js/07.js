// Orden de las operaciones
// En Javascript se utiliza la jeraquia comun de la matematica, usando el orden (), **, *, /, +, -

let resultado;

resultado = 20 + 30 * 2;

resultado = (100 + 200 + 100) * .2;

console.log(resultado);



// Incrementos

let puntaje = 10;

//Incremento en 1
puntaje++;  // 10
++puntaje;  // 12

// Decremento en 1
puntaje++;  // 10
--puntaje;  // 8


// Incremento y decremento personalizado
puntaje =+ 10;
puntaje =- 10;


console.log(puntaje);