//Funciones y metodos
/* existe une diferencia entre las funciones y los metodos */

const numero1 = 20;
const numero2 = "20";


/* Por lo regular, las funciones estan estructuradas por el nombre de la función seguido de parentesis y dentro de
ellos se ponen los parametros o argumentos de la funcion (por lo regular, variables) <<nombreFuncion(parametros)>> */ 

console.log(parseInt(numero2));     // parseInt() es una función

/* Los metodos en cambio tienen una estructura diferentes, por lo regular esta sucedidos por el nombre de la variable
seguidos por un punto y posteriormente el nombre del metodo <<variable.metodo()>> */

console.log(numero1.toString());    // .toString() es un metodo